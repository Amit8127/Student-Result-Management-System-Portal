    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String OperatingSystems = jTextField2.getText();
        String ComputerNetwork = jTextField3.getText();
        String ArtificialIntelligence = jTextField5.getText();
        String MachineLearning = jTextField6.getText();
        String JavaProgramming = jTextField7.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from students where rollno = '"+rollno+"'");
            if(rs.next() == true) {
                st.executeUpdate("insert into result(RollNo, Operating_Systems, Computer_Network, Artificial_Intelligence, Machine_Learning, Java_Programming) values ('"+rollno+"', '"+OperatingSystems+"', '"+ComputerNetwork+"', '"+ArtificialIntelligence+"', '"+MachineLearning+"', '"+JavaProgramming+"') ");
                JOptionPane.showMessageDialog(null, "Result is saved Succesfully!");
                setVisible(false);
                new InsertNewResult().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Student is not Registered!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

