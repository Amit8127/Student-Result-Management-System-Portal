    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String dob = jTextField2.getText();
        
        if(dob.equals("08/07/1999")) {
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from students inner join result where students.rollno = '"+rollno+"' and result.rollno = '"+rollno+"'");

                if(rs.next() == true) {
                    setVisible(false);
                    new StudentResultPage(rollno).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Incorrect Roll Number!");
                    setVisible(false);
                    new StudentResultPortalPage().setVisible(true); 
                }
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Incorrect Roll Number or Date of birth!");
            setVisible(false);
            new StudentResultPortalPage().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

