    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String rollNo = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fatherName = jTextField3.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","root");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into students(rollno, course, branch, name, gender, fathername) values('"+rollNo+"', '"+course+"', '"+branch+"', '"+name+"', '"+gender+"', '"+fatherName+"')");
            JOptionPane.showMessageDialog(null, "Data update Successfully!");
            setVisible(false);
            new AddNewStudent().setVisible(true);
        }
        catch(Exception e) {
            String ex = e.toString();
            
            if(ex.equals("com.mysql.cj.jdbc.exceptions.MysqlDataTruncation: Data truncation: Data too long for column 'name' at row 1")) {

                JOptionPane.showMessageDialog(null, "Name should be in 20 Character!");
                setVisible(false);
                new AddNewStudent().setVisible(true);
            }
            else if(ex.equals("com.mysql.cj.jdbc.exceptions.MysqlDataTruncation: Data truncation: Data too long for column 'rollno' at row 1")){

                JOptionPane.showMessageDialog(null, "Roll number should be in 15 Character!");
                setVisible(false);
                new AddNewStudent().setVisible(true);
            } else if(ex.equals("com.mysql.cj.jdbc.exceptions.MysqlDataTruncation: Data truncation: Data too long for column 'fathername' at row 1")){

                JOptionPane.showMessageDialog(null, "Father Name should be in 20 Character!");
                setVisible(false);
                new AddNewStudent().setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "Something is Wrong!");
                setVisible(false);
                new AddNewStudent().setVisible(true);
            }
        }  
    }//GEN-LAST:event_jButton6ActionPerformed

